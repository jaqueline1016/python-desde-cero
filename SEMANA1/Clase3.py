#OPERADORES 

## Operadores Aritméticos

#suma y resta (+ y -)
suma = 5 + 3
resta = 5 - 3

#multiplicación y división (* y /)
multiplicacion = 5 * 3
division = 5 / 3 # devuelve un float aunque sea una división exacta

#potencia (** )
potencia = 5 ** 3

#division baja (//)
division_baja = 5 // 3 # devuelve un entero, redondeando hacia abajo

#resto o modulo (%)
resto = 5 % 3 # devuelve el resto de la división

# tipo de dato 
tipod_de_dato = type(resto) # devuelve el tipo de dato del resultado

print(tipod_de_dato)

# Operadores de Comparación

# igualdad (==)
igualdad = (5 == 3) # devuelve False

# desigualdad (!=)
desigualdad = (5 != 3) # devuelve True

# mayor que (>)
mayor_que = (5 > 3) # devuelve True

# menor que (<)
menor_que = (5 < 3) # devuelve False

# mayor o igual que (>=)
mayor_o_igual = (5 >= 3) # devuelve True

# menor o igual que (<=)
menor_o_igual = (5 <= 3) # devuelve False

